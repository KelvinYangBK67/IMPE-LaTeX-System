for _language_code, _strings in ADD_FONT_TRANSLATIONS.items():
    TRANSLATIONS.setdefault(_language_code, {}).update(_strings)


def t(lang_code: str, key: str, **kwargs: object) -> str:
    strings = TRANSLATIONS.get(lang_code, TRANSLATIONS[DEFAULT_LANGUAGE])
    template = strings.get(key, TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key))
    if not kwargs:
        return template.replace("{{", "{").replace("}}", "}")
    return template.format(**kwargs)
